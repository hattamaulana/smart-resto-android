package id.ac.polinema.seameo.ecanteen.model;

import java.util.ArrayList;

public class KitchenQueueModel {
    private String key;
    private String name;
    private ArrayList<Menu> menus;

    public KitchenQueueModel(String name, ArrayList<Menu> menus) {
        this.name = name;
        this.menus = menus;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public ArrayList<Menu> getMenus() {
        return menus;
    }

    public void setMenus(ArrayList<Menu> menus) {
        this.menus = menus;
    }

    public static class Menu {
        private String name;
        private int count;

        public Menu(String name, int count) {
            this.name = name;
            this.count = count;
        }

        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getCount() {
            return count;
        }

        public void setCount(int count) {
            this.count = count;
        }
    }
}
